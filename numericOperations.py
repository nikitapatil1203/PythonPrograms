a = 10
b = 20
c = 10.5
d = 200.555

#addition
print(a+b)
print(a+d)

#Subtraction
print(d-c)

#Multiplication
print(b*a)
print(d*c)

#Division - Returns the exact result in decimal (float).
print(b/a)
print(d/c)

#Floor Division - (same as java division- returns quotient)
print(b//a)
print(d//c)

#Modulus - Returns the remainder 
print(b%a)
print(d%c)

#Exponent - Raises the left operand to the power of the right operand.
print(2 ** 3)
